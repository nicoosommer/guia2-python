#Ejercicio 2: Ingresar un número entero e indicar si es positivo o negativo. En el caso que sea un cero, indicar tal situación.

num=int(input("Ingrese un num entero: "))
if num==0:
    print("Ingreso un 0")
elif num<0:
    print("Ingreso un numero negativo")
else:
    print("Ingreso un numero positivo")
